import math

a = 3.35

print(math.floor(a))  # gives floor
print(math.ceil(a)) # gives ceil
print(round(a)) # rounds the value
print(round(4.5)) # tends to give a even number, so it rounds down
print(round(5.5)) # it rounds up for a even numberr
print(math.e) # value of e
print(math.pi) # value of pi
print(math.log(100, 10)) # log( of 100, base 10)
print(math.sin(math.pi / 2)) # gives the sin(x in radians)